#1. & : if both bits are 1 then consider 1 otherwise 0
print( 13 & 6)

#2 | : if both bits are 0 then consider 0, otherwise 1
print(13 | 6)

#3 ^(upper caret or cap) xor : consider 1 for different bits, o for same bits
print(13 ^ 6) 

#4 ~ (tild):
print(~10) # - (x + 1) :(10 +1) : -11

#5.<<(left shift) :
print(6<<2)
print(6<<4)

#6.>>(right shift)
print(6>>1)
print(6>>2)
